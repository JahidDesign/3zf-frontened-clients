// ─────────────────────────────────────────────────────────────────────────────
// TYPES
// ─────────────────────────────────────────────────────────────────────────────

export type Language     = 'en' | 'bn';
export type Lang         = Language;
export type Translations = typeof translations.en;
export type TranslationKeys = Translations;

// ─────────────────────────────────────────────────────────────────────────────
// CONTEXT
// ─────────────────────────────────────────────────────────────────────────────

interface I18nContextType {
  lang:    Lang;
  setLang: (l: Lang) => void;
  t:       Translations;
}

const I18nContext = createContext<I18nContextType>({
  lang:    'en',
  setLang: () => {},
  t:       translations.en,
});

// ─────────────────────────────────────────────────────────────────────────────
// FONT HELPER
// ─────────────────────────────────────────────────────────────────────────────

function applyLangFont(lang: Lang): void {
  if (typeof document === 'undefined') return;
  document.documentElement.lang = lang === 'bn' ? 'bn' : 'en';
  if (lang === 'bn') {
    document.body.classList.add('lang-bn');
    document.body.classList.remove('lang-en');
    document.body.style.fontFamily =
      'var(--font-noto-bengali), Noto Sans Bengali, sans-serif';
  } else {
    document.body.classList.add('lang-en');
    document.body.classList.remove('lang-bn');
    document.body.style.fontFamily = 'var(--font-inter), Inter, sans-serif';
  }
}

// ─────────────────────────────────────────────────────────────────────────────
// PROVIDER
// ─────────────────────────────────────────────────────────────────────────────

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>('en');

  useEffect(() => {
    const saved = localStorage.getItem('harmony-lang') as Lang | null;
    if (saved === 'en' || saved === 'bn') {
      setLangState(saved);
      applyLangFont(saved);
    }
  }, []);

  const setLang = (l: Lang): void => {
    setLangState(l);
    localStorage.setItem('harmony-lang', l);
    applyLangFont(l);
  };

  const contextValue: I18nContextType = {
    lang,
    setLang,
    t: translations[lang] as Translations,
  };

  return createElement(I18nContext.Provider, { value: contextValue }, children);
}

// ─────────────────────────────────────────────────────────────────────────────
// HOOKS
// ─────────────────────────────────────────────────────────────────────────────

export const useI18n = (): I18nContextType => useContext(I18nContext);
export const useLang = (): Lang             => useContext(I18nContext).lang;
export const useT   = (): Translations      => useContext(I18nContext).t;